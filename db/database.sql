--==================================================================================
-- CREATE DATABASE
--==================================================================================
create database car_booking;
grant all on database car_booking to admin;

\c car_booking;

--==================================================================================
-- CREATE EXTENSIONS
--==================================================================================
CREATE EXTENSION IF NOT EXISTS "uuid-ossp";

--==================================================================================
-- CREATE TABLES
--==================================================================================
create table cars (
  id uuid not null default uuid_generate_v4(),
  make varchar(255),
  model varchar(255),
  year int,  
  primary key(id)
);

--==================================================================================
-- INSERT DATA
--==================================================================================
insert into cars (make, model, year) 
